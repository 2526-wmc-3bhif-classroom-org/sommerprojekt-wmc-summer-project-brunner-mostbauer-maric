import express from "express";
import { StatusCodes } from "http-status-codes";
import { UserService } from "./user_service.js";
import type { User } from "./user_repository.js";

import { isAuthenticated, isAdmin } from "../middleware/auth_handlers.js";
import type { AuthRequest } from "../middleware/auth_handlers.js";
import { UserRole } from "../models/types.js";
import multer from "multer";
import path from "path";

export const userRouter = express.Router();
const userService: UserService = UserService.Instance;

// Configure multer for avatar uploads
const storage = multer.diskStorage({
  destination: (req, file, cb) => {
    cb(null, "avatars/");
  },
  filename: (req, file, cb) => {
    // Generate unique filename with timestamp
    const uniqueSuffix = Date.now() + "-" + Math.round(Math.random() * 1e9);
    cb(null, "avatar-" + uniqueSuffix + path.extname(file.originalname));
  },
});

const fileFilter = (req: any, file: any, cb: any) => {
  // Accept only image files
  const allowedMimes = ["image/jpeg", "image/png", "image/gif", "image/webp"];
  if (allowedMimes.includes(file.mimetype)) {
    cb(null, true);
  } else {
    cb(new Error("Only image files (JPEG, PNG, GIF, WebP) are allowed"), false);
  }
};

const upload = multer({
  storage,
  fileFilter,
  limits: { fileSize: 5 * 1024 * 1024 }, // 5MB limit
});


/**
 * @swagger
 * /api/users/{id}/avatar:
 *   post:
 *     summary: Upload a user avatar image
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *         description: The user ID
 *     requestBody:
 *       required: true
 *       content:
 *         multipart/form-data:
 *           schema:
 *             type: object
 *             properties:
 *               avatar:
 *                 type: string
 *                 format: binary
 *                 description: The avatar image file (JPEG, PNG, GIF, or WebP, max 5MB)
 *     responses:
 *       200:
 *         description: Avatar uploaded successfully
 *       400:
 *         description: Invalid input or unsupported file type
 *       401:
 *         description: Unauthorized
 *       403:
 *         description: Forbidden - Not authorized to upload avatar for this user
 *       404:
 *         description: User not found
 *       413:
 *         description: File too large
 */
userRouter.post("/:id/avatar", isAuthenticated, (req, res, next) => {
  upload.single("avatar")(req, res, (err: any) => {
    if (err) {
      // Handle multer errors
      if (err.code === "LIMIT_FILE_SIZE") {
        res
          .status(StatusCodes.BAD_REQUEST)
          .json({ error: { message: "File too large. Maximum size is 5MB" } });
      } else if (err.message && err.message.includes("Only image files")) {
        res
          .status(StatusCodes.BAD_REQUEST)
          .json({ error: { message: err.message } });
      } else {
        res
          .status(StatusCodes.BAD_REQUEST)
          .json({ error: { message: err.message || "File upload failed" } });
      }
      return;
    }
    next();
  });
}, (req, res) => {
  try {
    const userId = parseInt(req.params.id);
    const authReq = req as AuthRequest;

    if (!req.file) {
      res
        .status(StatusCodes.BAD_REQUEST)
        .json({ error: { message: "No file uploaded" } });
      return;
    }

    const avatarPath = req.file.path;

    const result = userService.updateUserAvatar(
      userId,
      avatarPath,
      authReq.payload?.user.UserId || 0,
      authReq.payload?.user.Role as UserRole
    );

    if (result.error) {
      res.status(result.status).json({ error: result.error });
    } else {
      res.status(result.status).json({ data: result.data });
    }
  } catch (error) {
    res
      .status(StatusCodes.INTERNAL_SERVER_ERROR)
      .json({ error: { message: (error as Error).message } });
  }
});

